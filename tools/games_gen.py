def verify_and_build():
    out = []
    print("=" * 68)
    ok_all = True
    for g in GAMES:
        board = chess.Board()
        sans = []
        bad = None
        for i, (san, note) in enumerate(g["moves"]):
            try:
                mv = board.parse_san(san)
            except Exception as e:
                bad = "ply %d '%s': %s" % (i + 1, san, e)
                break
            sans.append(san)
            board.push(mv)
        status = "OK "
        if bad:
            status = "NG!"; ok_all = False
        else:
            last = g["moves"][-1][0]
            if last.endswith("#") and not board.is_checkmate():
                status = "NG!(最終手が#だが詰んでいない)"; ok_all = False
            elif last.endswith("#") and board.is_checkmate():
                status = "OK (詰みも確認)"
        print("%s %-22s %2d手 %s" % (status, g["id"], len(g["moves"]), bad or ""))
        if bad:
            continue
        out.append({
            "id": g["id"], "title": g["title"],
            "white": g["white"], "black": g["black"],
            "year": g["year"], "event": g["event"], "result": g["result"],
            "opening": g["opening"], "intro": g["intro"], "takeaway": g["takeaway"],
            "moves": [{"san": s, "note": n} for s, n in g["moves"]],
        })
    print("=" * 68)
    print("verified %d / %d games" % (len(out), len(GAMES)))
    return out, ok_all


def main():
    games, ok = verify_and_build()
    path = sys.argv[1] if len(sys.argv) > 1 else os.path.join(os.path.dirname(__file__), "..", "js", "games.js")
    body = "// 自動生成: tools/games_gen.py (python-chessで全手検証済み)\n"
    body += "// 解説はすべて独自に書き下ろし。他者の注釈の転載はしていない。\n"
    body += "const FAMOUS_GAMES = " + json.dumps(games, ensure_ascii=False, indent=1) + ";\n"
    with open(path, "w", encoding="utf-8") as f:
        f.write(body)
    print("wrote", path)
    if not ok:
        print("!!! 検証に失敗したゲームがあります。修正してください。")
        sys.exit(1)


