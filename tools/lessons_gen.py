def verify_and_build():
    out = []
    print("=" * 68)
    ok_all = True
    for L in LESSONS:
        board = chess.Board(L["fen"]) if L["fen"] else chess.Board()
        if not board.is_valid():
            print("NG! %-16s 不正なFEN: %s" % (L["id"], board.status()))
            ok_all = False
            continue
        bad = None
        for i, (san, note) in enumerate(L["moves"]):
            try:
                mv = board.parse_san(san)
            except Exception as e:
                bad = "ply %d '%s': %s" % (i + 1, san, e)
                break
            board.push(mv)
        status = "OK "
        if bad:
            status = "NG!"; ok_all = False
        else:
            last = L["moves"][-1][0]
            if last.endswith("#"):
                status = "OK (詰み確認)" if board.is_checkmate() else "NG!(詰んでいない)"
                if not board.is_checkmate():
                    ok_all = False
        print("%s %-14s [%s] %2d手 %s" % (status, L["id"], L["cat"], len(L["moves"]), bad or ""))
        if bad:
            continue
        out.append({
            "id": L["id"], "cat": L["cat"], "title": L["title"],
            "fen": L["fen"], "intro": L["intro"], "takeaway": L["takeaway"],
            "moves": [{"san": s, "note": n} for s, n in L["moves"]],
        })
    print("=" * 68)
    print("verified %d / %d lessons" % (len(out), len(LESSONS)))
    return out, ok_all


def main():
    lessons, ok = verify_and_build()
    path = sys.argv[1] if len(sys.argv) > 1 else os.path.join(os.path.dirname(__file__), "..", "js", "lessons.js")
    body = "// 自動生成: tools/lessons_gen.py (python-chessで検証済み)\n"
    body += "// 解説はすべて独自に書き下ろし。\n"
    body += "const LESSONS = " + json.dumps(lessons, ensure_ascii=False, indent=1) + ";\n"
    with open(path, "w", encoding="utf-8") as f:
        f.write(body)
    print("wrote", path)
    if not ok:
        print("!!! 検証に失敗したレッスンがあります。")
        sys.exit(1)


if __name__ == "__main__":
    main()
